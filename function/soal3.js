const processSentence = (name,age,addres,hoby)=>{
    return `Nama saya ${name}, umur saya ${age} tahun, alamat saya di ${addres}, dan saya punya hobby yaitu ${hoby}!`
}

let name = "Agus";
let age = 30;
let address = "Jln. Malioboro, Yogjakarta";
let hobby = "gaming";

let fullSentence = processSentence(name,age,address,hobby);
